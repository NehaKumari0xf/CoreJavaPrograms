public class nestedIfElseMessage {
    public static void main(String arr[])
    {
        int marks=35;
        if(marks>=90)
        System.out.print("A+ Grade");
        else if(marks>=80 )
        System.out.print("A Grade");
        else if(marks>=70 )
        System.out.print("B Grade");
        else if(marks>=60 )
        System.out.print("C Grade");
        else if(marks>=30)
        System.out.print("D Grade");
        
        else
        System.out.print("Fail");
    }
}
